public class STEPBannerApp {
    public static void main(String[] args) {

        // Each letter is 9 characters wide
        // 7 lines high

        // Line 1
        System.out.println(
                "  *****  " +  // O
                "  *****  " +  // O
                "  ****** " +  // P
                "  *****  "    // S
        );

        // Line 2
        System.out.println(
                " *     * " +
                " *     * " +
                " *     * " +
                " *     * "
        );

        // Line 3
        System.out.println(
                " *     * " +
                " *     * " +
                " *     * " +
                " *       "
        );

        // Line 4
        System.out.println(
                " *     * " +
                " *     * " +
                "  ****** " +
                "  *****  "
        );

        // Line 5
        System.out.println(
                " *     * " +
                " *     * " +
                " *       " +
                "       * "
        );

        // Line 6
        System.out.println(
                " *     * " +
                " *     * " +
                " *       " +
                " *     * "
        );

        // Line 7
        System.out.println(
                "  *****  " +
                "  *****  " +
                " *       " +
                "  *****  "
        );
    }
}